    private void tabelaFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaFuncionariosMouseClicked
        JTable source = (JTable) evt.getSource();
        String id = source.getModel().getValueAt(source.getSelectedRow(), 0) + "";

        cad_funcionariosDAO funcionariosDAO = new cad_funcionariosDAO();
        Cad_funcionarios funcionario = funcionariosDAO.getFuncById(id);
        
        // System.out.println("ID: " + funcionario.getId() + ", NOME: " + funcionario.getNome());
        jTextID.setText(String.valueOf(funcionario.getId()));
        jTextCPF.setText(funcionario.getCpf());
        jTextMatricula.setText(String.valueOf(funcionario.getMatricula()));
        jTextNome.setText(funcionario.getNome());
        jTextSalario.setText(String.valueOf(funcionario.getSalario()));
        // jTextSenhaFunc.setText();
    }//GEN-LAST:event_tabelaFuncionariosMouseClicked

