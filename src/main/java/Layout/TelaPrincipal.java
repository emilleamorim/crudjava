    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        // TODO add your handling code here:
        String cpf = jTextCPF.getText();
        int matricula = Integer.parseInt(jTextMatricula.getText());
        String nome = jTextNome.getText();
        double salario = Integer.parseInt(jTextSalario.getText());
        String senha = jTextSenhaFunc.getText();
        int acessosis = Integer.parseInt(jComboAcessosis.getSelectedItem().toString());
        String cargo = jComboCargo.getSelectedItem().toString();
        
        Cad_funcionarios cad_func = new Cad_funcionarios();
        cad_func.setCargo(cargo);
        cad_func.setCpf(cpf);
        cad_func.setMatricula(matricula);
        cad_func.setNome(nome);
        cad_func.setPermite_login(acessosis);
        cad_func.setSalario(salario);
        cad_func.setSenha(senha);
        
        cad_funcionariosDAO cad_DAO = new cad_funcionariosDAO();
        cad_DAO.insert(cad_func);
        
        jTextCPF.setText("");
        jTextMatricula.setText("");
        jTextNome.setText("");
        jTextSalario.setText("");
        jTextSenhaFunc.setText("");
        
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

