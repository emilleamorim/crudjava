    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        // TODO add your handling code here:
        cad_funcionariosDAO funcionariosDAO = new cad_funcionariosDAO();
        List<Cad_funcionarios> listaFuncionarios = funcionariosDAO.getFuncByName(jTextNome.getText());
        
        DefaultTableModel modelo = (DefaultTableModel) tabelaFuncionarios.getModel();
        modelo.setRowCount(0);

        for (Cad_funcionarios c : listaFuncionarios ) {
            System.out.println("Nome: " + c.getNome());
            modelo.addRow(new Object[]{
                c.getId(),
                c.getMatricula(),
                c.getNome(),
                c.getCpf()
            });
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

